# -------------------------------------------
# ---- Modifying Array Elements in Python ---
# -------------------------------------------
# Examples

grades = [ 10, 20, 30, 40, 50] # size: 5 # indexz: 0, 1, 2, 3, 4

print(grades) # [10, 20, 30, 40, 50]

grades[0] = 15 # modifying the first element 

grades[2] = 35 # modifying the third element

print(grades) # [15, 20, 35, 40, 50]