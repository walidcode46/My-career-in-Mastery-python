# For Loop Example: Printing Numbers and Characters
# This program demonstrates the use of for loops in Python.
# The first for loop iterates over a range of numbers from 1 to 11 (exclusive) and prints each number.
# The second for loop iterates over each character in the string "Walid" and prints each character.
# --------------------------------------------------------

# start: 1
# end: 11 (length - 1)
for number in range(1, 12):
     print(number) 

# start: W
# end: a (EOL)
for character in "Walid":
    print(character)