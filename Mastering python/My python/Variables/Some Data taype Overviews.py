# ----------------------------------------
# type()
# all Data in python is object 
# ----------------------------------------

print(type(10))  # int =>    Integer
print(type(455)) # int =>   Integer
print(type(-8))  # int =>  Integer

print(type(6.8))       # float =>  floating point Number
print(type(66.78))     # float =>  loating point Number
print(type(7898.9987)) # float => loating point Number

print(type("hello python"))   # str => string 

print(type([1,2,3,4,5]))      # list => List

print(type((1,2,3,4,5)))      #tuple => Tuple

print(type({"One" : 1, "Two" : 2, "Three" : 3 })) # dict => Dictionary

print(2 == 4) # bool => Boolean
print(2 < 4)  # bool => Boolean
print(2 > 4)  # bool => Boolean
print(type(True)) # bool => Boolean
print(type(False)) # bool => Boolean
print(type(3 + 4j)) # complex => Complex Number
